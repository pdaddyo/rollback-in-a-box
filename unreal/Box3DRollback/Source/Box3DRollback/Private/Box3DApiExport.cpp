// Re-export the Box3D C API from this module's DLL.
//
// Box3D lives in a prebuilt STATIC library. In Unreal's modular (editor)
// builds, any game module that also linked that static lib would get its own
// private copy of Box3D's process-global state — the b3_worlds array, the
// length-units global, the assert hook. A world created through this plugin
// would then be invisible to raw b3* calls made from the game module (null
// deref on first use), and b3SetLengthUnitsPerMeter would set the wrong
// copy's global.
//
// /EXPORT both pulls each symbol out of the static lib and exports it from
// this DLL (a plain __declspec(dllexport) on a declaration does neither).
// Raw-API consumers compile with BOX3D_EXPORT=__declspec(dllimport) and
// resolve against this DLL — ONE copy of Box3D per process. The list is the
// intersection of the headers' B3_API declarations with the symbols actually
// present in the built lib (some declarations are compiled out in Release,
// e.g. b3InternalAssert, b3World_DumpShapeBounds).

